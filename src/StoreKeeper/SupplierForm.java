                tblModel.setValueAt(id, index, 0);
                tblModel.setValueAt(name, index, 1);
                tblModel.setValueAt(cat, index, 2);
                tblModel.setValueAt(vol, index,3);
                tblModel.setValueAt(brand, index,4);
                tblModel.setValueAt(descr, index, 5);

