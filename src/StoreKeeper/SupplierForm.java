    private void DeleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBttnActionPerformed
        // TODO add your handling code here:
        
        String id=IDText.getText();

        try {
            // TODO add your handling code here:

            if(IDText.getText().equals("")){

                JOptionPane.showMessageDialog(null,"Please, Select Data First.");

            }
            else if(IDText.getText() != null){

                Class.forName("com.mysql.jdbc.Driver");
                Connection connectme=java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/wine_liquor_store", "root","");

                PreparedStatement delete = (PreparedStatement) connectme
                .prepareStatement("delete from supplier  where Supplier_ID=? ");

                delete.setString(1,id);
                delete.execute();

                DefaultTableModel tblModel=(DefaultTableModel) SupplierTable.getModel();

                if(SupplierTable.getSelectedRowCount()==1){

                    tblModel.removeRow(SupplierTable.getSelectedRow());

                    JOptionPane.showMessageDialog(null,"Data Have Deleted Successfully.");

                IDText.setText("");
                NameText.setText("");
                CatText.setText("");
                Valumetext.setText("+250");
                BrandText.setText("");
                Description.setText("");
                SaveBttn.setEnabled(true);

                }
                
                }

        

        } catch (ClassNotFoundException | SQLException ex) {
            //Logger.getLogger(ProductForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Can't Delete This Record,Can Affect Some Transations Occured!");
           
        }
        
    }//GEN-LAST:event_DeleteBttnActionPerformed

