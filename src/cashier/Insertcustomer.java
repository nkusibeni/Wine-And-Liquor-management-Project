    private void SearchBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBttnActionPerformed
        // TODO add your handling code here:
       
        String name=SearchText.getText();
        String mobile=SearchText.getText();
        
        
        try {
            // TODO add your handling code here:
            
            if(SearchText.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Please,Insert Something in Search Field.");
            }
            
            else{

            Class.forName("com.mysql.jdbc.Driver");
            Connection connectme=DriverManager.getConnection("jdbc:mysql://localhost:3306/wine_liquor_store", "root","");
          
           PreparedStatement select = (PreparedStatement) connectme
           .prepareStatement("select * from customer where Customer_Name=? OR Phone=?");
          
           
           select.setString(1,name);
           select.setString(2,mobile);
           ResultSet rslt= select.executeQuery();
           
           if(rslt.next()){
               
               String Customer_ID=rslt.getString(1);
               String CustomerName=rslt.getString(2);
               String Adress=rslt.getString(3);
               String Phone=rslt.getString(4);
               
             
                IDText.setText(Customer_ID);
                CustomerText.setText(CustomerName);
                AdressText.setText(Adress);
                PhoneText.setText(Phone);
                
                InsertBttn.setEnabled(false);
                
           }
           
           else{
               JOptionPane.showMessageDialog(null,"No Data For Your Search");
           }
               
           }
           
          
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Insertcustomer.class.getName()).log(Level.SEVERE, null, ex);
            
        }

        
       
        
        
    }//GEN-LAST:event_SearchBttnActionPerformed

