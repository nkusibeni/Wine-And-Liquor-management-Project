    private void InsertBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertBttnActionPerformed
        // TODO add your handling code here:
        
        String name=CustomerText.getText();
        String Adress=AdressText.getText();
        String mobile=PhoneText.getText();
        
       
        if (evt.getSource()==InsertBttn){
            
            if(CustomerText.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please,Fill Information First.");
        }
            else{
        try {
            // TODO add your handling code here:
            

            Class.forName("com.mysql.jdbc.Driver");
            Connection connectme=DriverManager.getConnection("jdbc:mysql://localhost:3306/wine_liquor_store", "root","");
          
           PreparedStatement insert = (PreparedStatement) connectme
           .prepareStatement("insert into customer values(null,?,?,?)");
           
           
           insert.setString(1,name);
           insert.setString(2,Adress);
           insert.setString(3,mobile);
           insert.execute();
           
            PreparedStatement slt = (PreparedStatement) connectme
           .prepareStatement("select Customer_ID from customer where Customer_Name=? and Phone=?");
           
           slt.setString(1,name);
           slt.setString(2,mobile);
           ResultSet rslt= slt.executeQuery();
           if(rslt.next()){
               
            String CustID=rslt.getString(1);
            String FID= UserText.getText();
            
            PreparedStatement query = (PreparedStatement) connectme
           .prepareStatement("insert into sales values(null,NOW(),?,?)");
            
            query.setString(1,CustID);
           query.setString(2,FID);
           query.execute();
           
           PreparedStatement slt2 = (PreparedStatement) connectme
           .prepareStatement("select Sales_ID from sales where Customer_ID=? ORDER BY Sales_Date DESC LIMIT 1");
           
           slt2.setString(1,CustID);
           ResultSet rslt2= slt2.executeQuery();
           
           if(rslt2.next()){
               
               
               
               JOptionPane.showMessageDialog(null,"Data inserted successfully!");
               
                SaleDetails sd= new SaleDetails();
                sd.setVisible(true);
                sd.pack();
                sd.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
                SaleDetails.UserText.setText(UserText.getText());
                SaleDetails.SaleText.setText(rslt2.getString(1));
           }
               
           }
         
         
          
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Insertcustomer.class.getName()).log(Level.SEVERE, null, ex);
        }

            }
        }
        
    }//GEN-LAST:event_InsertBttnActionPerformed

